export const detectTime = (timestamp: number)=> {
  let msec = timestamp;
  const hh = Math.floor(msec / 1000 / 60 / 60);
  msec -= hh * 1000 * 60 * 60;
  const mm = Math.floor(msec / 1000 / 60);
  msec -= mm * 1000 * 60;
  const ss = Math.floor(msec / 1000);
  msec -= ss * 1000;
  return {h: hh, m: mm, s: ss, ms: msec}
}
